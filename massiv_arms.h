bool chkAms(int n)
{
    // Введення масиву та призначення усіх елементів 0
     int dig[10]={0};
     // Введення змінних
    int i,p=0,c=0,s=0,m=n;
    // Введення циклу та перевірка умови якщо, так виконуємо те,
    // що у фігурних дужках
    while (n > 0)
    {
       dig[p++]=n%10;
       c++;
       n=n/10;
    }      
    for (i=0; i<p; i++) s+=ipow(dig[i],c);
    //Повертаємо значення змінної
    return (m==s);
}
